package co.jjw.prc;

public class AssignmentOperatorExample {

	public static void main(String[] args) {
		// 대입 연산자
		
		int result = 0;
		
		result += 10;
		System.out.println("result = " + result);
		
		result -= 5;
		System.out.println("result = " + result);
		
		result *= 3;
		System.out.println("result = " + result);
		
		result /= 5;
		System.out.println("result = " + result);
		
		result %= 3;
		System.out.println("result = " + result);
	}
}
